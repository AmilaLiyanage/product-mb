package org.dna.mqtt.wso2;

import org.dna.mqtt.moquette.proto.messages.AbstractMessage;

/**
 * Constructs a ping request that will indicate the existence of a subscription
 * This is called to trigger the task to indicate rejection acks if any
 */
public class MQTTPingRequest extends AbstractMessage {
    /**
     * Will include the id of the channel the ping request was made on
     */
    String channelId;

    public String getChannelId() {
        return channelId;
    }

    public void setChannelId(String channelId) {
        this.channelId = channelId;
    }


}
